#Operações Aritméticas

#Soma
print("4 + 4 =", 4 + 4)

#Subtração
print("4 - 3 =", 4 - 3)

#Multiplicação
print("3 * 3 =", 3 * 3)

#Divisão
print("3 / 2 =", 3 / 2)

#Módulo
print("10 % 3 =", 10 % 3)

#Função Type

#Type Int
print("type(5) =", type(5))

#Type Float
print("type(5.0) =", type(5.0))

#Type String
a = 'Eu estou estudando na Data Science Academy'
print("type(a) =", type(a))

#Operações com Números Float

#Float + Float
print("3.1 + 6.4 =", 3.1 + 6.4)

#Int + Float
print("4 + 4.0 =", 4 + 4.0)

#Int + Int
print("4 + 4 =", 4 + 4)

#Int / Int (Resultado será Float)
print("4 / 2 =", 4  / 2)

#Int // Int (Resultado será Int)
print("4 // 2 =", 4 // 2)

#Int / Float
print("4 / 3.0 =", 4 / 3.0)

#Int // Float
print("4 // 3.0 =", 4 // 3.0)

#Conversão

#Int para Float
print("float(9) =", float(9))

#Float para Int
print("int(6.0) =", int(6.0))

#Float para Int
print("int(6.5) =", int(6.5))

#Hexadecimal e Binário

print("hex(394) =", hex(394))
print("hex(217) =", hex(217))

print("bin(286) =", bin(286))
print("bin(390) =", bin(390))

#Funções abs, round e pow

#Abs - Retorna o valor absoluto
print("abs(-8) =", abs(-8))

#Round - Retorna o valor com as casas decimais definidas
print("round(3.14151922, 2) =", round(3.14151922, 2))

#Potência
print("pow(4, 2) =", pow(4, 2))
print("pow(4, 3) =", pow(4, 3))